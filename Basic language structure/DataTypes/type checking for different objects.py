"""Demonstration of type checking for different Python objects."""

# Define variables of different types
VAR1 = 15                   # Integer
VAR2 = "Welcome in Python"  # String
VAR3 = [12, 23, 43]         # List

# Print the type of each variable with a clear message
print("VAR1 is of type:", type(VAR1))
print("VAR2 is of type:", type(VAR2))
print("VAR3 is of type:", type(VAR3))