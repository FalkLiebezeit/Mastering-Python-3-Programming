"""Demonstration of set creation and usage in Python."""

# Create a set using curly brackets
new_set1 = {101, 20.15, "Seema", "Priya"}
print("The set values of set 1:", new_set1)
print("Type of object set 1:", type(new_set1))

# Create a set using the set() constructor
new_set2 = set({55, 100, 88, 200, "Apple", "Mango"})
print("The set values of set 2:", new_set2)
print("Type of object set 2:", type(new_set2))