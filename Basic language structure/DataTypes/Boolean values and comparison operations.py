"""Demonstration of Boolean values and comparison operations in Python."""

# Assign Boolean values directly
x = True
y = False
print("x =", x)
print("y =", y)

# Assign Boolean values using comparison operators
x = 5 < 2   # False, because 5 is not less than 2
y = 5 > 2   # True, because 5 is greater than 2
print("5 < 2 evaluates to:", x)
print("5 > 2 evaluates to:", y)