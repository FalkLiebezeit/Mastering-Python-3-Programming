# Loop through a list using a while loop

flowers = ['lily', 'rose', 'jasmine', 'sunflower']  # List of flower names
n = len(flowers)  # Get the number of elements in the list
i = 0  # Initialize the loop counter

# --- Iterate through the list using a while loop ---
while i < n:
    print(flowers[i])
    i += 1  # Increment the counter