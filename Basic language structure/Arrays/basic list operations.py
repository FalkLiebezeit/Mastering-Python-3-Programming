# Demonstration of basic list operations in Python

# --- Initialize the list ---
a = [56, 57, 89]

# --- Access and print the first element (index 0) ---
i = a[0]  # 56 is at the 0th index
print("The value at 0th position is", i)

# --- Append a new element to the end of the list ---
a.append(90)  # Adds 90 at the last position

# --- Access and print the newly added element (index 3) ---
i = a[3]
print("The value at 3rd position is", i)

# --- Find and print the length of the list ---
k = len(a)
print('The length of array is', k)